// Fonction utilitaire pour insérer les icônes dans le DOM
export function insertFeatherIcon(iconName, targetElement, customAttributes = {}) {
  if (FeatherIcons[iconName]) {
    const iconSvg = FeatherIcons[iconName];
    const parser = new DOMParser();
    const doc = parser.parseFromString(iconSvg, 'image/svg+xml');
    const svgElement = doc.documentElement;
    
    // Appliquer les attributs personnalisés
    Object.keys(customAttributes).forEach(attr => {
      svgElement.setAttribute(attr, customAttributes[attr]);
    });
    
    targetElement.innerHTML = '';
    targetElement.appendChild(svgElement);
  }
}

// Fonction pour créer un élément d'icône directement
export function createFeatherIcon(iconName, customAttributes = {}) {
  if (FeatherIcons[iconName]) {
    const iconSvg = FeatherIcons[iconName];
    const parser = new DOMParser();
    const doc = parser.parseFromString(iconSvg, 'image/svg+xml');
    const svgElement = doc.documentElement;
    
    // Appliquer les attributs personnalisés
    Object.keys(customAttributes).forEach(attr => {
      svgElement.setAttribute(attr, customAttributes[attr]);
    });
    
    return svgElement;
  }
  return null;
}